// if, else and else if condition 

var restul = 50;

if(restul >= 80 && restul <=100){
    console.log('A+');
}

else if(restul >= 70 && restul <=79){
    console.log('A');
}

else if(restul >= 60 && restul <=69){
    console.log('B+');
}

else if(restul >= 50 && restul <=59){
    console.log('B');
}

else if(restul >= 40 && restul <=49){
    console.log('C');
}

else if(restul > 33 && restul <=39){
    console.log('D');
}
else{
    console.log('F');
}